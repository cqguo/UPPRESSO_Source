package sdk.Bean;

public class UserInfo {
    String ID;
    String sk_server;
    String sk_client;
    String register_uid;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getSk_server() {
        return sk_server;
    }

    public void setSk_server(String sk_server) {
        this.sk_server = sk_server;
    }

    public String getSk_client() {
        return sk_client;
    }

    public void setSk_client(String sk_client) {
        this.sk_client = sk_client;
    }

    public String getRegister_uid() {
        return register_uid;
    }

    public void setRegister_uid(String register_uid) {
        this.register_uid = register_uid;
    }
}
